package org.openmrs.web.dwr;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.openmrs.Orderable;

public class OrderableListItem {
	
	protected final Log log = LogFactory.getLog(getClass());
	
	private String id;
	
	private String name;
	
	private String description;
	
	public OrderableListItem() {
	}
	
	public OrderableListItem(Orderable<?> orderable) {
		if (orderable != null) {
			this.id = orderable.getUniqueIdentifier();
			this.name = orderable.getName();
			this.description = orderable.getDescription();
		}
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
}
